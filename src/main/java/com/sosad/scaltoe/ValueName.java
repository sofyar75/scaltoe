package com.sosad.scaltoe;

public class ValueName {
	
	private String value;
	private String name;
	
	public ValueName(String value, String name) {
		this.value = value;
		this.name = name;
	}
	
	public String getValue() {
		return value;
	}
	public String getName() {
		return name;
	}

}
